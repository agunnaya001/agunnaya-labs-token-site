export const aglTokenAddress = process.env.NEXT_PUBLIC_AGL_TOKEN_ADDRESS as `0x${string}`
export const stakingContractAddress = (process.env.NEXT_PUBLIC_STAKING_CONTRACT_ADDRESS ||
  '0x0000000000000000000000000000000000000000') as `0x${string}`

export function getTokenContract(walletClient: any) {
  return getContract({
    address: aglTokenAddress,
    abi: AGL_TOKEN_ABI,
    client: walletClient,
  })
}

export function getStakingContract(walletClient: any) {
  return getContract({
    address: stakingContractAddress,
    abi: STAKING_ABI,
    client: walletClient,
  })
}
